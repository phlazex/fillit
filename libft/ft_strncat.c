#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	c;

	c = 0;
	i = 0;
	while (*(s1 + i))
		i++;
	while (*s2 && c < n)
	{
		s1[i] = (char)*s2;
		i++;
		s2++;
		c++;
	}
	s1[i] = '\0';
	return (s1);
}
