#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	ans;

	i = 0;
	j = 0;
	while (*(dst + i))
		i++;
	while (*(src + j))
		j++;
	if (i > size)
		return (j + size);
	ans = i + j;
	j = 0;
	while (src[j] && i < size - 1)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (ans);
}
